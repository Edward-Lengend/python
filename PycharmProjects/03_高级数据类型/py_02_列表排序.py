name_list = ["ZhangSan", "LiSi", "WangWu"]
num_list = [6, 8, 4, 1, 10]

# 升序
name_list.sort()
print(name_list)
num_list.sort()
print(num_list)

# 降序
name_list.sort(reverse=True)
print(name_list)
num_list.sort(reverse=True)
print(num_list)

# 逆序
num_list = [6, 8, 4, 1, 10]
num_list.reverse()
print(num_list)
